type Vars = Record<string, string | number>

export function translate(lang: Lang, key: Keys, vars?: Vars): string {
  const str = T[lang][key]
  if (!vars) return str
  return str.replace(/{(\w+)}/g, (_, k) => String(vars[k] ?? ''))
}
